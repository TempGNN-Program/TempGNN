      echo "Commit: $(git rev-parse HEAD)"
      echo "Branch: $(git rev-parse --abbrev-ref HEAD)"
      echo "--- git status --short ---"
      git status --short || true
    else
      echo "Not inside a git repository"
    fi
  else
    echo "git not found"
  fi
  echo

  echo "[Experiment Defaults]"
  echo "Batch size: 1000"
  echo "Precision: FP32"
