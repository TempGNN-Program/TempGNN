#!/usr/bin/env bash
set -euo pipefail

OUT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
OUT_FILE="${OUT_DIR}/information.txt"

{
  echo "TempGNN Artifact Environment Information"
  echo "======================================="
  echo
  echo "[Timestamp]"
  date
  echo

  echo "[System]"
  echo "Hostname: $(hostname)"
  echo "OS:"
  if command -v lsb_release >/dev/null 2>&1; then
    lsb_release -d 2>/dev/null || true
  fi
  echo "Kernel: $(uname -r)"
  echo "Architecture: $(uname -m)"
  echo

  echo "[CPU]"
  if command -v lscpu >/dev/null 2>&1; then
    lscpu
  else
    echo "lscpu not found"
  fi
  echo

  echo "[Memory]"
  if command -v free >/dev/null 2>&1; then
    free -h
  else
    echo "free not found"
  fi
  echo

  echo "[GPU]"
  if command -v nvidia-smi >/dev/null 2>&1; then
    nvidia-smi
  else
    echo "nvidia-smi not found"
  fi
  echo

  echo "[FPGA]"
  if command -v xbutil >/dev/null 2>&1; then
    xbutil examine || true
  else
    echo "xbutil not found"
  fi
  echo

  echo "[Compilers and Toolchains]"
  echo "--- gcc ---"
  if command -v gcc >/dev/null 2>&1; then
    gcc --version
  else
    echo "gcc not found"
  fi
  echo

  echo "--- g++ ---"
  if command -v g++ >/dev/null 2>&1; then
    g++ --version
  else
    echo "g++ not found"
  fi
  echo

  echo "--- python ---"
  if command -v python3 >/dev/null 2>&1; then
    python3 --version
  else
    echo "python3 not found"
  fi
  echo

  echo "--- nvcc ---"
  if command -v nvcc >/dev/null 2>&1; then
    nvcc --version
  else
    echo "nvcc not found"
  fi
  echo

  echo "--- vitis ---"
  if command -v vitis >/dev/null 2>&1; then
    vitis -version || true
  else
    echo "vitis not found in PATH"
  fi
  echo

  echo "--- vivado ---"
  if command -v vivado >/dev/null 2>&1; then
    vivado -version || true
  else
